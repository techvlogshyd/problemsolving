public class StringDataType {
    public static void main(String[] args) {
        //A data type in java, not a primitive type, Its actually a class, but it enjoys a bit of favoritism in Java to make it easier to use than a regular class
        //Sequence of chars
      String myString="This is  a string";
        System.out.println(myString);
        System.out.println(" \u00A9 2019");
        int myInt=10;
        System.out.println(myString+myInt);//Integer value gets appended when performing add operation with string

    }
}
